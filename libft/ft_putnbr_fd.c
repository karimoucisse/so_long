#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == 2147483647)
		write(fd, "2147483647", 10);
	else if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		if (n > 9)
		{
			ft_putnbr_fd((n / 10), fd);
			ft_putchar_fd((n % 10) + '0', fd);
		}
		else
			ft_putchar_fd(n + '0', fd);
	}
}
